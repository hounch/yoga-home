"use client";

import Image from "next/image";

export type CardData = {
  imageSrc?: string;
  name?: string;
  title?: string;
  type?: string;
  cost?: string;
  description?: string;
  duration?: string;
  className?: string;
};

type CardType = "Card1" | "Card2" | "Card3" | "Card4" | "Card5" | "Card6";

type CardProps = {
  type: CardType;
  data: CardData;
};

export default function Card({ type, data }: CardProps) {
  const getImageSize = () => {
    switch (type) {
      case "Card1":
        return { width: 373, height: 191 };
      case "Card2":
        return { width: 500, height: 300 };
      case "Card3":
        return { width: 266, height: 358 };
      case "Card4":
        return { width: 368, height: 212 };
      default:
        return { width: 300, height: 200 };
    }
  };

  return (
    <div className={`${data.className || ""} h-full`}>
      <article className="bg-[#EEE5DC] rounded-[20px] h-full flex flex-col">
        {/* Изображение для всех типов кроме Card5 */}
        {data.imageSrc && (
          <div className="rounded-[20px] overflow-hidden">
            <Image
              src={data.imageSrc}
              alt={data.title || "Изображение"}
              {...getImageSize()}
              className={`w-full object-cover ${
                type === "Card1"
                  ? "h-[191px]"
                  : type === "Card2"
                  ? "h-[300px]"
                  : type === "Card3"
                  ? "h-[358px] w-[266px]"
                  : type === "Card4"
                  ? "h-[212px] w-[268px]"
                  : type === "Card5"
                  ? "w-[369px] h-[250px]"
                  : type === "Card6"
                  ? "w-[369px] h-[250px]"
                  : "h-[500px]"
              }`}
            />
          </div>
        )}

        {/* Контент для разных типов карточек */}
        <div className="p-4 flex flex-col flex-grow text-left">
          {data.title && (
            <h3 className="text-[26px] text-[#494542] mb-3">{data.title}</h3>
          )}

          {/* Блок мета-данных для Card1 */}
          {type === "Card1" && (
            <div className="flex items-center text-[#494542] text-[14px] font-medium mb-4">
              {data.duration && (
                <div className="flex items-center mr-4">
                  <ClockIcon />
                  <span className="ml-[5px]">{data.duration}</span>
                </div>
              )}
              {data.type && (
                <div className="flex items-center mr-4">
                  <GroupIcon />
                  <span className="ml-[5px]">{data.type}</span>
                </div>
              )}
              {data.cost && (
                <div className="flex items-center">
                  <PriceIcon />
                  <span className="ml-[5px]">{data.cost}</span>
                </div>
              )}
            </div>
          )}

          {type === "Card4" && (
            <div className="flex items-center text-[#494542] text-[14px] font-medium mb-4">
              <div className="flex items-center mr-4">
                <TgIcon />
                <span className="ml-[5px]">telegram</span>
              </div>
              <div className="flex items-center mr-4">
                <InstIcon />
                <span className="ml-[5px]">instagram</span>
              </div>
            </div>
          )}

          {/* Описание для Card1 и Card4 */}
          {data.description && (
            <p className="text-[#494542] text-[14px]">{data.description}</p>
          )}

          {/* Специфичный контент для Card5 */}
          {type === "Card5" && data.cost && (
            <>
              <div className="bg-[#859974] mt-[90px] w-[369px] h-[250px] rounded-[20px] text-[#EEE5DC] absolute">
                <div className="text-[32px] text-left leading-[130%] w-[314px] mx-auto mt-[30px]">
                  <p>{data.name} </p>
                </div>
                <div className="flex justify-between items-end w-[314px] ml-[30px] mb-[21px] mt-auto absolute bottom-0">
                  <p className="text-2xl font-bold">{data.cost}</p>
                  <LogoIcon />
                </div>
              </div>
            </>
          )}
          {/* Специфичный контент для Card6 */}
          {type === "Card6" && data.cost && (
            <>
              <div className="bg-[#859974] mt-[90px] w-[369px] h-[250px] rounded-[20px] text-[#EEE5DC] absolute">
                <div className="text-[32px] text-left leading-[130%] w-[314px] mx-auto mt-[30px]">
                  <p>{data.name} </p>
                </div>
                <div className="flex justify-between items-end w-[314px] ml-[30px] mb-[21px] mt-auto absolute bottom-0">
                  <p className="text-2xl font-bold">{data.cost}</p>
                  <LogoIcon />
                </div>
              </div>
            </>
          )}
        </div>
      </article>
    </div>
  );
}

